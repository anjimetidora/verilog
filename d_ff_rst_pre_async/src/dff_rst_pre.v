	module dff_rst_pre(input data,clk,rst,pre,
											output reg q);
			always @(posedge clk or negedge rst or posedge  pre)
				begin:always_block
					if(!rst)
						begin:not_reset
							q<=1'b0;
						end   //not reset
					else
						begin:else_blk
							case(pre)
								1'b1:
									begin:pre1_blk
										q<=1'b1;
									end	//pre1_blk
								1'b0:
									begin:pre0_blk
										q<=data;
									end	//pre0_blk
								default:
									begin:if_pre_x_z
										q<=q;
									end	//if_pre_x_z
							endcase
						 end	//else_blk
				end//alw_blk
	endmodule








