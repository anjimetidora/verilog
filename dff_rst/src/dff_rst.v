module dff_reset_n(input data,clock,reset_n,
									 output reg q);
	always @(posedge clock or negedge reset_n)
		begin:alw_blk
			case(reset_n)							//different cases
					1'b0:
						begin:reset0_blk
							q<=1'b0;
						end	//reset0_blk
					1'b1:
						begin:reset1_blk
							q<=data;
						end	//reset1_blk
					default:
						begin:reset_xz_blk
							q<=1'bx;
						end
					endcase									//end different cases
			end	//alw_blk
endmodule
									
