module dff_sync_preset(input data,
	                  input preset,
										input clock,
										output reg q);
	/*always @(posedge clock)
		begin:alw_blk
	  	if(preset)
				begin:preset1_blk
	  			q<=1'b1;
				end	//preset1_blk
			else
				begin:preset0_blk
					q<=data;
				end	//preset0_blk
		end //alw_blk
endmodule*/


	always@(posedge clock)
		begin:alw_blk
			case(preset)
				1'b1:
					begin:preset1_blk
						q<=1'b1;
					end	//preset1_blk
				1'b0:
					begin:preset0_blk
						q<=data;
					end	//preset0_blk
				default:
					begin:if_pre_x_z
						q<=q;
					end	//if_pre_x_z
			endcase
		end
		endmodule




