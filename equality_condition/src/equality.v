`timescale 1ns/10ps
module equality(input reg [2:0]a,
								input reg [2:0]b,
								output reg c);

assign c=a==b?'b1:'b0;

endmodule


module tb;

reg [2:0]a,b;
reg c;

equality dut_insta(.a(a),.b(b),.c(c));

initial 
	begin
			a='b110;b='b100;
		#5	a='b1x0;b='b1x0;
		#5	a='b1x1;b='b10x;
		#5	a='b110;b='b110;
		#5	a='b1x0;b='b10z;
		#5	a='b1x0;b='b100;
		#5	a='b1x0;b='b100;
			#5 a='b1x0;b='b100;
		#50 ;$finish;
	end
	initial 
	begin
			$monitor("time=%0t,c=%0b",$time,c);
		end
		endmodule
